#include <unistd.h>
#include <stdio.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2", 2);
		nb = 14748648;
	}
	if (nb < 0)
	{
		nb *= (-1);
		write(1, "-", 1);
	}
	if (nb >= 0)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	if (nb < 10)
	{
		ft_putchar(nb + '0');
	}
}

int main(void)
{
	ft_putnbr(-2147483648);
	return (0);
}
